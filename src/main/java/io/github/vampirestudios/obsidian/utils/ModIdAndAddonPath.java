package io.github.vampirestudios.obsidian.utils;

public class ModIdAndAddonPath {

	public String modId;
	public String path;

	public ModIdAndAddonPath(String modId, String path) {
		this.modId = modId;
		this.path = path;
	}

	public String getModId() {
		return modId;
	}

	public String getPath() {
		return path;
	}
}
